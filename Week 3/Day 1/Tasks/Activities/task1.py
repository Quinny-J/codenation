# Write a program that uses a for loop to print hello world 13 times.
# Then convert it to a while loop that does the same job

# i = 0 at this point then will go up one by one so it loops 13 times
for i in range(13):
    # Print hello world each time
    print("hello world")

# Create a var to store our count
count = 0
# count = 0 at this point then will go up one by one so it loops 13 times
while count < 13:
    # Print hello world each time
    print("hello world")
    # We need too add to our count
    count += 1