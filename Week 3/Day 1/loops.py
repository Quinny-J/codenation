# Loops/Task 1
# Write a program that uses a for loop to print all the numbers from 1 to 10.

# i = 0 at this point then will go up one by one so it loops 10 times
for i in range(1, 11): # 1-10
    # print the output each time
    print(i)

for i in range(1, 100, 10): # min, max, step
    # print the output each time
    print(i)

for i in range(5, 11): # 5-10
    # print the output each time
    print(i)

for i in range(5, 11, 1): # 5-10 step of 1 but one is set by default no need for it in this use case
    # print the output each time
    print(i)        

for i in range(2, 12, 2): # +2 each time
    # print the output each time
    print(i)


